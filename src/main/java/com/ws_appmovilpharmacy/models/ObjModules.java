package com.ws_appmovilpharmacy.models;

public class ObjModules {
	private String modCod;
	private String modNom;
	private String modPath;
	private String modImg;
	private String modSup;

	public String getModCod() {
		return modCod;
	}

	public void setModCod(String modCod) {
		this.modCod = modCod;
	}

	public String getModNom() {
		return modNom;
	}

	public void setModNom(String modNom) {
		this.modNom = modNom;
	}

	public String getModPath() {
		return modPath;
	}

	public void setModPath(String modPath) {
		this.modPath = modPath;
	}

	public String getModImg() {
		return modImg;
	}

	public void setModImg(String modImg) {
		this.modImg = modImg;
	}

	public String getModSup() {
		return modSup;
	}

	public void setModSup(String modSup) {
		this.modSup = modSup;
	}

}
